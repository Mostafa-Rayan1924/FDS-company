let btns = document.querySelectorAll(".lang button");
btns.forEach((item) => {
  item.addEventListener("click", (e) => {
    addActive(e);
    let itemLang = item.getAttribute("data-btn");

    localStorage.setItem("lang", itemLang);

    const elements = document.querySelectorAll("[data-lang]");

    elements.forEach((ele) => {
      const translationKey = ele.getAttribute("data-lang");

      ele.textContent = translate[itemLang][translationKey];
    });
    if (itemLang == "en") {
      document.body.style.direction = "ltr";
    } else {
      document.body.style.direction = "rtl";
    }
  });
});

if (localStorage.length != "") {
  let localItem = localStorage.getItem("lang");

  const elements = document.querySelectorAll("[data-lang]");

  elements.forEach((ele) => {
    const translationKey = ele.getAttribute("data-lang");

    ele.textContent = translate[localItem][translationKey];

    if (localItem == "en") {
      document.body.style.direction = "ltr";

      document.querySelector(".en").classList.add("active");

      document.querySelector(".ar").classList.remove("active");
    } else if (localItem == "ar") {
      document.body.style.direction = "rtl";

      document.querySelector(".en").classList.remove("active");

      document.querySelector(".ar").classList.add("active");
    }
  });
}
function addActive(ele) {
  btns.forEach((item) => {
    item.classList.remove("active");
    ele.currentTarget.classList.add("active");
  });
}
